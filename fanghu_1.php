<b>一、 尽量减少外出活动</b></font></br>

（一）避免去疾病正在流行的地区。</br>
 
（二）减少到人员密集的公共场所活动，尤其是空气流动性差的地方，</br>例如公共浴池、温泉、影院、网吧、KTV、商场、车站、机场等。</br>
 <font color="#6633CC" size="6" >
<b>二、个人防护和手卫</b></font></br>
（一）外出佩戴口罩。外出前往公共场所、就医和乘坐公共交通工具时，佩戴医用外科口罩或N95口罩。</br>
 
（二）随时保持手卫生。减少接触公共场所的公共物品和部位；从公共场所返回、咳嗽手捂之后、饭前便后，用洗手液或香皂流水洗手，或者使用含酒精成分的免洗洗手液；避免用手接触口鼻眼；打喷嚏或咳嗽时，用手肘衣服遮住口鼻。</br>
<font color="#6633CC" size="6" >
<b>三、健康监测与就医</b></font></br>
 
（一）主动做好个人与家庭成员的健康监测，自觉发热时要主动测量体温。</br>
 
（二）若出现可疑症状，应主动戴上口罩及时就近就医。若出现新型冠状病，应根据病情，及时到医疗机构就诊。并尽量避免乘坐地铁、公共汽车等交通工具，避免前往人群密集的场所。就诊时应主动告诉医生自己的相关疾病流行地区的旅行居住史，以及发病后接触过什么人，配合医生开展相关调查。</br>
 <font color="#6633CC" size="6" >
<b>四、保持良好卫生和健康习惯。</b></font></br>
 
（一）居室勤开窗，经常通风。</br>
 
（二）不随地吐痰，口鼻分泌物用纸巾包好，弃置于有盖垃圾箱内。</br>
 
（三）注意营养，适度运动。</br>
 
（四）不要接触、购买和食用野生动物；尽量避免前往售卖活体动物的市场。</br>
 
（五）家庭备置体温计、医用外科口罩或N95口罩、家用消毒用品等物资。</br>
